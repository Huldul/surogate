                <h1 class="main_title">программа донорства ооцитов</h1>
                <p>
                    На сегодняшний день у некоторых женщин в яичниках, вследствие патологических состояний (повреждение яичников при химиотерапии и радиологическом лечении, преждевременный климакс и др.), не происходит рост фолликулов и созревание яйцеклеток. В таких случаях яйцеклетки получают у другой здоровой женщины-донора, оплодотворяют эти донорские яйцеклетки спермой мужа бесплодной пациентки или спермой донора и полученные эмбрионы переносят в матку бесплодной женщине либо суррогатной матери. Этот процесс называется ДОНОРСТВО ооцитов (яйцеклеток).
                </p>
                <p>
                    Такой метод также может быть использован при удалённых яичниках или при высоком риске передачи ребенку генетических болезней. Показание к программе донорства определяют специалисты (врачи).
                </p>
                <p>
                    Центр имеет обширную базу доноров ооцитов (яйцеклеток) соответствующих всем требованиям законодательства и медицины, все девушки не старше 30 лет имеют хорошую генетику, подтвержденную медицинскими заключениями. Также проходят обширный спектр анализов на отсутствие инфекционных заболеваний, онкологических клеток и т.д. Все доноры приятной внешности, с высшим образованием и имеют необходимый запас фолликулов для участия в программе донорства ооцитов (яйцеклеток).
                </p>
                <p>
                    При заключении договора на оказание услуг по подбору донора ооцитов (яйцеклеток), достаточно присутствия только супруги заказчика. В договоре прописываются сроки, оговоренные с заказчицей и ее индивидуальные требования к донору ооцитов (яйцеклеток) при подборе. Донорство ооцитов (яйцеклеток) полностью анонимное со стороны донора, т.е заказчик знает своего донора, но донору информация о заказчике не передается, вся документация проходит через центр.
                </p>
                <blockquote>
                    Донору выплачивается определенная компенсация в день пункции на восстановление здоровья, сумма зависит от наличия опыта в программе «Донорства ооцитов (яйцеклеток)» и от количества переданных яйцеклеток.
                </blockquote>
